<p style="color:#4ECDC4;">Toyota Supra (яп. トヨタ・スープラ) — серийный спортивный автомобиль, выпускающийся компанией Toyota с 1978 года.</p>

<p>Дизайн Supra был перенят от Toyota Celica, но кузов стал длиннее и шире. Начиная с середины 1986 года, Supra отделилась от Celica, став самостоятельной моделью. В связи с этим Toyota перестала использовать префикс Celica, и автомобиль стал именоваться просто Supra. Первое, второе и третье поколения Supra собирались на заводе в Тахаре, а четвёртое и пятое поколение — на заводе в городе Тоёта в Японии.</p>

<p>Supra также имеет связь с Toyota 2000GT, от которой к ней перешел двигатель. На автомобили первых трёх поколений устанавливались двигатели M-серии от Toyota Crown и 2000GT. На все четыре поколения Supra устанавливались рядные 6-цилиндровые двигатели. Шасси получило код «А».</p>

<p>Toyota разработала собственный логотип для Supra. Этот логотип использовался до января 1986 года, когда появилось первое самостоятельное поколение. Новый логотип был похож по размеру, имел оранжевую надпись на красном фоне, но без изображения дракона. Этот логотип, в свою очередь, использовался до 1991 года, когда Toyota перешли на логотип компании.</p>

<p>В 1998 году Toyota прекратила продажи Supra в США, а в 2002 году было официально прекращено производство в Японии.</p>

<p>Четыре поколения получили обозначения A40, A60, A70, A80. Официальные названия модификаций автомобилей Toyota относятся только к кодам шасси. Toyota использует название Mark II для обозначения своих автомобилей на платформе X, которые включают модели Mark II, Cressida, Chaser и Cresta</p>

